package com.arcane.LeagueOfLegend.domain;

public class SearchVO {

    private String championNo;
    private String championNm;
    private String championRegion;
    private String championPosition;

    public String getChampionNo() {
        return championNo;
    }

    public void setChampionNo(String championNo) {
        this.championNo = championNo;
    }

    public String getChampionNm() {
        return championNm;
    }

    public void setChampionNm(String championNm) {
        this.championNm = championNm;
    }

    public String getChampionRegion() {
        return championRegion;
    }

    public void setChampionRegion(String championRegion) {
        this.championRegion = championRegion;
    }

    public String getChampionPosition() {
        return championPosition;
    }

    public void setChampionPosition(String championPosition) {
        this.championPosition = championPosition;
    }
}
